<h5 style="text-align: center;"><strong><em><em><br></em></em></strong></p><p style="text-align: center;"><strong>1.- Lineamientos del Tecnológico Nacional de México</strong></h4>
<br>
<h5 style="text-align:justify">De acuerdo al manual de lineamientos de los Tecnológicos Nacionales de México, capítulo 14 del lineamiento para la titulación integral, apartado 14.4.1.2 de los requisitos de titulación integral se establece:<br>
<br>
La acreditación de un programa de lengua extranjera, presentando un certificado o constancia emitido por: a) una Institución u Organismo Nacional o Internacional con validez oficial o b) las Coordinaciones de Lenguas Extranjeras de los Institutos Tecnológicos registradas oficialmente por la Secretaría Académica, de Investigación e Innovación del TecNM. El certificado o constancia debe validar la capacidad de entender, elaborar y comunicar de manera oral y escrita nociones estándar en el ámbito personal, así como profesional, de acuerdo al Nivel B1 del Marco Común Europeo de Referencia (MCER) para el caso del idioma inglés.<br>

</h5 >
<!-- ==== LINEAMIENTO 2 ==== -->

<h5 style="text-align: center;"><strong><em><em><br></em></em></strong></p><p style="text-align: center;"><strong>2.-Modalidades de acreditación de la lengua extranjera</strong></h5>
<br>
<h5 style="text-align:justify">El estudiante podrá elegir alguna de las siguientes opciones para acreditar la lengua extranjera, y al finalizar se le otorgara una constancia de acreditación para su proceso de titulación.<br>
<br>
2.1- Modalidad CURSO ESCOLARIZADO vigente a partir del ciclo escolar 2015.<br>
<br>
La acreditación de la lengua extranjera tendrá como objetivo que los estudiantes obtengan el nivel B1 del marco común europeo de referencia (MCER) dividido en 10 módulos de trabajo, en donde se establecerán las partes gramáticas y verbales de la lengua y tambien se preparara para la certificación del examen equivalente al nivel B1 del MCER.

La modalidad de acreditación a partir de la generación 2015-B será escolarizada, dividida en 6 horas semanales durante 16 semanas, haciendo un total de 90 horas por semestre. 
<br>
<br>
2.2 Acreditación con certificado nivel B1
<br>
 El alumno puede acreditar inglés entregando un certificado oficial vigente de alguna institución con validez internacional o nacional reconocida, al momento de la liberación (ya sea Cambridge o TOEFL IBT) que avale su nivel de inglés en las 4 habilidades (reading, listening, writing, speaking) con un nivel B1 del MCER.
</h5 >

<!-- ==== LINEAMIENTO 3 ==== -->

<h5 style="text-align: center;"><strong><em><em><br></em></em></strong></p><p style="text-align: center;"><strong>3. Medios de curso Autodidacta u otra institución</strong></h5>
<br>
<h5 style="text-align:justify">Si el estudiante ha adquirido la competencia lingüística del idioma por otro medio, tal como: una CLE sin registro, de manera autodidacta o en cualquier otra institución, deberá aplicar un examen de cuatro habilidades en una CLE registrada, demostrando el nivel B1.<br>

</h5 >


