<h5 style="text-align: center;"><strong><em><em><br></em></em></strong></p><p style="text-align: center;"><strong>4. Nuestra oferta de liberación </strong></h5>
<br>
<h5 style="text-align:justify">Si el candidato no ha cursado inglés y considera que tienes un nivel equivalente al B1 o superior, se oferta la opción de OOPT de Oxford que cubre tres habilidades del idioma mediante una evaluación online, si se aprueba con un nivel equivalente al B1 o superior, el candidato pasa al examen oral por parte de la coordinación de lenguas extranjeras la cual consta de una entrevista y un ejercicio de habilidad en parejas, evaluados por dos sínodos.<br>
